/**
 * PlantUML diagram generation from static analysis data.
 * Four diagram types:
 *  - fileDiagram   : one file — component, props, imports
 *  - moduleDiagram : whole project — files as classes, import arrows, grouped by folder
 *  - journeyDiagram: whole project — routes as states, nav links as transitions
 *  - classDiagram  : whole project — TypeScript interfaces + components that use them
 */

import type { ParsedFile } from '@/types/graph';
import { FILE_TYPE_CONFIG } from '@/types/graph';

// ─── Utilities ────────────────────────────────────────────────────────────────

/** Make a safe PlantUML identifier from any string */
function id(s: string): string {
  return s.replace(/[^A-Za-z0-9]/g, '_').replace(/^_+/, '') || '_root';
}

/** Strip # from colour for class-level annotations */
function col(hex: string): string {
  return hex.replace('#', '');
}

function typeBg(type: string): string {
  return col(FILE_TYPE_CONFIG[type as keyof typeof FILE_TYPE_CONFIG]?.bg ?? '#1E293B');
}

function typeLabel(type: string): string {
  return FILE_TYPE_CONFIG[type as keyof typeof FILE_TYPE_CONFIG]?.label ?? type;
}

